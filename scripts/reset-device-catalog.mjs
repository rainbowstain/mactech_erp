import pg from "pg";
import { getPostgresSslConfig } from "../lib/postgres-ssl.js";
import { BRAND_MODELS, PART_TYPES } from "../lib/seed-data.js";

const { Client } = pg;

if (!process.env.DATABASE_URL) {
  throw new Error("DATABASE_URL no esta configurado.");
}

const client = new Client({
  connectionString: process.env.DATABASE_URL,
  ssl: getPostgresSslConfig(process.env.DATABASE_URL),
});

async function resetCatalog() {
  await client.query("begin");

  await client.query(`
    create table if not exists repuestos (
      id integer generated by default as identity primary key,
      nombre text not null,
      estado integer default 1
    )
  `);

  const expectedBrands = Object.keys(BRAND_MODELS);
  for (const [brand, models] of Object.entries(BRAND_MODELS)) {
    const existingBrand = await client.query("select id from equipos where lower(nombre) = lower($1) order by id limit 1", [
      brand,
    ]);
    const brandId =
      existingBrand.rows[0]?.id ||
      (await client.query("insert into equipos (nombre, estado) values ($1, 1) returning id", [brand])).rows[0].id;
    await client.query("update equipos set nombre = $1, estado = 1 where id = $2", [brand, brandId]);

    for (const model of models) {
      const existingModel = await client.query(
        "select id from dispositivos where modelo = $1 and lower(nombre) = lower($2) order by id limit 1",
        [brandId, model]
      );
      if (existingModel.rows[0]) {
        await client.query("update dispositivos set nombre = $1, estado = 1 where id = $2", [model, existingModel.rows[0].id]);
      } else {
        await client.query("insert into dispositivos (nombre, modelo, estado) values ($1, $2, 1)", [model, brandId]);
      }
    }
  }

  for (const name of PART_TYPES) {
    const existingPart = await client.query("select id from repuestos where lower(nombre) = lower($1) order by id limit 1", [name]);
    if (existingPart.rows[0]) {
      await client.query("update repuestos set nombre = $1, estado = 1 where id = $2", [name, existingPart.rows[0].id]);
    } else {
      await client.query("insert into repuestos (nombre, estado) values ($1, 1)", [name]);
    }
  }

  await client.query("update repuestos set estado = 0 where nombre <> all($1::text[])", [PART_TYPES]);

  await client.query("update equipos set estado = 0 where nombre <> all($1::text[])", [expectedBrands]);

  await client.query("commit");
}

try {
  await client.connect();
  await resetCatalog();
  console.log("Catalogo actualizado sin borrar ordenes, inventario ni ventas.");
} catch (error) {
  await client.query("rollback").catch(() => {});
  throw error;
} finally {
  await client.end();
}
