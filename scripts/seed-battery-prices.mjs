import pg from "pg";
import { getPostgresSslConfig } from "../lib/postgres-ssl.js";

const { Client } = pg;

const BATTERY_PRICES = [
  { model: "iPhone 11", cost: 13000, repairValue: 29900 },
  { model: "iPhone 12 mini", cost: 19000, repairValue: 59900 },
  { model: "iPhone 12", cost: 19000, repairValue: 59900 },
  { model: "iPhone 12 Pro", cost: 19000, repairValue: 59900 },
  { model: "iPhone 12 Pro Max", cost: 23000, repairValue: 69900 },
  { model: "iPhone 13", cost: 19000, repairValue: 59900 },
  { model: "iPhone 13 mini", cost: 19000, repairValue: 59900 },
  { model: "iPhone 13 Pro", cost: 22000, repairValue: 69900 },
  { model: "iPhone 13 Pro Max", cost: 25000, repairValue: 79900 },
  { model: "iPhone 14", cost: 22000, repairValue: 79900 },
  { model: "iPhone 14 Pro", cost: 25000, repairValue: 79900 },
  { model: "iPhone 14 Pro Max", cost: 28000, repairValue: 79900 },
  { model: "iPhone 15", cost: 25000, repairValue: 79900 },
  { model: "iPhone 15 Pro", cost: 28000, repairValue: 89900 },
  { model: "iPhone 15 Pro Max", cost: 30000, repairValue: 99900 },
];

function barcodeFor(model) {
  return `MTT-BAT-${model.toUpperCase().replace(/[^A-Z0-9]+/g, "-").replace(/^-|-$/g, "")}`;
}

async function ensureSchema(client) {
  await client.query(`
    create table if not exists repuestos (
      id integer generated by default as identity primary key,
      nombre text not null,
      estado integer default 1
    )
  `);
  await client.query("alter table inventario_items add column if not exists dispositivo_id integer references dispositivos(id)");
  await client.query("alter table inventario_items add column if not exists repuesto_id integer references repuestos(id)");
}

async function findBatteryPartId(client) {
  const existing = await client.query(
    "select id from repuestos where lower(nombre) in ('bateria', 'batería') order by estado desc, id limit 1"
  );
  if (existing.rows[0]) return existing.rows[0].id;

  const created = await client.query("insert into repuestos (nombre, estado) values ('Bateria', 1) returning id");
  return created.rows[0].id;
}

async function main() {
  if (!process.env.DATABASE_URL) throw new Error("DATABASE_URL no esta configurado.");

  const client = new Client({
    connectionString: process.env.DATABASE_URL,
    ssl: getPostgresSslConfig(process.env.DATABASE_URL),
  });

  await client.connect();
  try {
    await client.query("begin");
    await ensureSchema(client);
    const batteryPartId = await findBatteryPartId(client);

    let created = 0;
    let updated = 0;
    for (const price of BATTERY_PRICES) {
      const device = await client.query(
        `
          select d.id
          from dispositivos d
          join equipos e on e.id = d.modelo
          where e.nombre = 'Apple' and lower(d.nombre) = lower($1)
          order by d.id
          limit 1
        `,
        [price.model]
      );
      if (!device.rows[0]) throw new Error(`No existe el dispositivo Apple ${price.model}.`);

      const deviceId = device.rows[0].id;
      const productName = `Apple ${price.model} Bateria`;
      const existing = await client.query(
        `
          select id
          from inventario_items
          where area = 'taller' and dispositivo_id = $1 and repuesto_id = $2
          order by estado desc, id
          limit 1
        `,
        [deviceId, batteryPartId]
      );

      if (existing.rows[0]) {
        await client.query(
          `
            update inventario_items
            set
              marca = 'Apple',
              producto = $1,
              valor_original = $2,
              descuento = 0,
              valor_venta = $3,
              notas = 'Bateria certificada',
              estado = 1,
              updated_at = now()
            where id = $4
          `,
          [productName, price.cost, price.repairValue, existing.rows[0].id]
        );
        updated += 1;
      } else {
        await client.query(
          `
            insert into inventario_items (
              area, marca, producto, codigo_barra, valor_original, descuento,
              valor_venta, cantidad, stock_minimo, ubicacion, notas, estado,
              dispositivo_id, repuesto_id
            )
            values (
              'taller', 'Apple', $1, $2, $3, 0,
              $4, 0, 0, null, 'Bateria certificada', 1,
              $5, $6
            )
          `,
          [productName, barcodeFor(price.model), price.cost, price.repairValue, deviceId, batteryPartId]
        );
        created += 1;
      }
    }

    await client.query("commit");
    console.log(`Baterias pobladas. Creadas: ${created}. Actualizadas: ${updated}.`);
  } catch (error) {
    await client.query("rollback").catch(() => {});
    throw error;
  } finally {
    await client.end();
  }
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
