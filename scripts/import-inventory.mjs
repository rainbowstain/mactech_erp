import { execFileSync } from "node:child_process";
import { existsSync } from "node:fs";
import pg from "pg";
import { getPostgresSslConfig } from "../lib/postgres-ssl.js";

const { Pool } = pg;

const DEFAULT_XLSX = "/Users/eduardo/Downloads/Inventario_Simple (2).xlsx";

function decodeXml(value = "") {
  return value
    .replace(/&amp;/g, "&")
    .replace(/&lt;/g, "<")
    .replace(/&gt;/g, ">")
    .replace(/&quot;/g, "\"")
    .replace(/&apos;/g, "'");
}

function unzipText(xlsxPath, filePath) {
  return execFileSync("unzip", ["-p", xlsxPath, filePath], { encoding: "utf8" });
}

function columnName(cellRef) {
  return cellRef.match(/[A-Z]+/)?.[0] || "";
}

function cellValue(cellXml, sharedStrings) {
  const type = cellXml.match(/t="(\w+)"/)?.[1];
  const value = cellXml.match(/<v>(.*?)<\/v>/s)?.[1] || "";
  if (type === "s") return sharedStrings[Number(value)] || "";
  if (type === "inlineStr") return decodeXml(cellXml.match(/<t[^>]*>(.*?)<\/t>/s)?.[1] || "");
  return decodeXml(value);
}

function readWorkbookRows(xlsxPath) {
  if (!existsSync(xlsxPath)) {
    throw new Error(`No existe el archivo: ${xlsxPath}`);
  }

  const sharedXml = unzipText(xlsxPath, "xl/sharedStrings.xml");
  const sheetXml = unzipText(xlsxPath, "xl/worksheets/sheet1.xml");
  const sharedStrings = [...sharedXml.matchAll(/<si>(.*?)<\/si>/gs)].map((match) =>
    decodeXml(match[1].replace(/<t[^>]*>(.*?)<\/t>/gs, (_, text) => text).replace(/<[^>]+>/g, ""))
  );

  const rows = [];
  for (const rowMatch of sheetXml.matchAll(/<row[^>]*r="(\d+)"[^>]*>(.*?)<\/row>/gs)) {
    const rowNumber = Number(rowMatch[1]);
    if (rowNumber === 1) continue;

    const row = {};
    for (const cellMatch of rowMatch[2].matchAll(/<c[^>]*r="([A-Z]+\d+)"[^>]*>.*?<\/c>/gs)) {
      row[columnName(cellMatch[1])] = cellValue(cellMatch[0], sharedStrings).trim();
    }

    if (row.A || row.B) rows.push(row);
  }

  return rows;
}

function asInt(value) {
  const number = Number(value || 0);
  return Number.isFinite(number) ? Math.round(number) : 0;
}

function asDiscount(value) {
  const number = Number(value || 0);
  return Number.isFinite(number) ? number : 0;
}

async function ensureSchema(client) {
  await client.query(`
    create table if not exists repuestos (
      id integer generated by default as identity primary key,
      nombre text not null,
      estado integer default 1
    )
  `);
  await client.query(`
    create table if not exists inventario_items (
      id integer generated by default as identity primary key,
      area text not null default 'productos' check (area in ('productos', 'taller')),
      marca text,
      producto text not null,
      codigo_barra text not null unique,
      valor_original integer default 0,
      descuento numeric(8, 4) default 0,
      valor_venta integer default 0,
      cantidad integer default 0,
      stock_minimo integer default 0,
      ubicacion text,
      notas text,
      estado integer default 1,
      dispositivo_id integer references dispositivos(id),
      repuesto_id integer references repuestos(id),
      created_at timestamptz default now(),
      updated_at timestamptz default now()
    )
  `);
  await client.query("alter table inventario_items add column if not exists dispositivo_id integer references dispositivos(id)");
  await client.query("alter table inventario_items add column if not exists repuesto_id integer references repuestos(id)");
  await client.query(`
    create table if not exists inventario_movimientos (
      id integer generated by default as identity primary key,
      item_id integer references inventario_items(id) on delete cascade,
      tipo text not null check (tipo in ('entrada', 'salida', 'ajuste')),
      cantidad integer not null,
      cantidad_anterior integer not null,
      cantidad_nueva integer not null,
      motivo text,
      responsable text,
      created_at timestamptz default now()
    )
  `);
  await client.query("create index if not exists inventario_items_area_idx on inventario_items(area)");
  await client.query("create index if not exists inventario_items_codigo_barra_idx on inventario_items(codigo_barra)");
  await client.query("create index if not exists inventario_items_producto_idx on inventario_items(producto)");
  await client.query("create index if not exists inventario_items_dispositivo_id_idx on inventario_items(dispositivo_id)");
  await client.query("create index if not exists inventario_items_repuesto_id_idx on inventario_items(repuesto_id)");
  await client.query("create index if not exists inventario_movimientos_item_id_idx on inventario_movimientos(item_id)");
}

async function main() {
  const xlsxPath = process.argv[2] || DEFAULT_XLSX;
  if (!process.env.DATABASE_URL) throw new Error("DATABASE_URL no esta configurado");

  const rows = readWorkbookRows(xlsxPath);
  const pool = new Pool({
    connectionString: process.env.DATABASE_URL,
    ssl: getPostgresSslConfig(process.env.DATABASE_URL),
  });
  const client = await pool.connect();

  try {
    await client.query("begin");
    await ensureSchema(client);

    let imported = 0;
    for (const [index, row] of rows.entries()) {
      const barcode = `MTP${String(index + 1).padStart(6, "0")}`;
      const product = row.B || "Producto sin nombre";
      await client.query(
        `
          insert into inventario_items (
            area,
            marca,
            producto,
            codigo_barra,
            valor_original,
            descuento,
            valor_venta,
            cantidad,
            estado,
            updated_at
          )
          values ('productos', $1, $2, $3, $4, $5, $6, $7, 1, now())
          on conflict (codigo_barra) do update
          set
            marca = excluded.marca,
            producto = excluded.producto,
            valor_original = excluded.valor_original,
            descuento = excluded.descuento,
            valor_venta = excluded.valor_venta,
            cantidad = excluded.cantidad,
            updated_at = now()
        `,
        [row.A || null, product, barcode, asInt(row.C), asDiscount(row.D), asInt(row.E), asInt(row.F)]
      );
      imported += 1;
    }

    await client.query("commit");
    console.log(`Inventario importado: ${imported} productos desde ${xlsxPath}`);
  } catch (error) {
    await client.query("rollback");
    throw error;
  } finally {
    client.release();
    await pool.end();
  }
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
